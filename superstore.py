import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

df=pd.read_csv("/content/Sample - Superstore.csv", encoding='latin1')

df.head(5)

df.info()

df.describe()

df.drop_duplicates(inplace=True)

df.info()

grouped=df.groupby('Segment')[['Profit','Sales']].sum()
grouped['Sales - Profit'] = grouped['Sales'] - grouped['Profit']
grouped.style.format('{:,.2f}')

x=grouped.index
y=grouped['Profit']
plt.bar(x,y)
plt.xlabel('Segment')
plt.ylabel('Profit')
plt.title('Profit by Segment')
plt.show()

"""***Analysis of profit distribution reveals that the Consumer segment outperforms both Corporate and Home Office. This suggests that the majority of our successful business transactions are coming from individual end-consumers.***"""

grouped_order = df.groupby('Segment')['Quantity'].apply(lambda x: x.mode().iloc[0])
print(grouped_order)

grouped_cust =df.groupby('Customer ID')['Customer ID'].count()
cust_state = grouped_cust.apply(lambda x: 'Bronze' if x < 5 else ('Silver' if x <= 10 else ('Gold' if x<=20 else 'Platinum')))
grouped_state = cust_state.value_counts()
print(grouped_state)

x= grouped_state.index
y= grouped_state
colors = ['#FFD700', '#C0C0C0', '#E5E4E2', '#CD7F32']
plt.pie(y, labels=x, autopct='%1.1f%%', colors=colors, startangle=140)

plt. title('Customer state distribution')
plt.show()

"""-The store has a good retention rate, as the number of Gold and Platinum customers is quite high.

-Customers tend to buy three items per order. We can use this insight to offer discounts for customers who purchase three or more items.


"""

groupcat=df.groupby('Category')['Profit'].sum()
