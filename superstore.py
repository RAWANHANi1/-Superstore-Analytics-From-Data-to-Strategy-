df[['Discount','Profit','Sales']].corr()

sns.heatmap(df[['Discount','Profit','Sales']].corr(),annot=True)

"""Discounts do not have a significant impact on Sales; in other words, they do not drive volume. However, they clearly decrease the overall Profit."""

df['Order Date']= pd.to_datetime(df['Order Date'],format='%m/%d/%Y')
yearly_profit= df.groupby(df['Order Date'].dt.year)[['Profit','Discount']].sum()
print(yearly_profit)
sns.lineplot(x=yearly_profit.index,y=yearly_profit['Profit'])
plt.xlabel('Year')
plt.ylabel('Profit')
plt.title('Yearly Profit Trend')
plt.grid(True)
plt.xticks(yearly_profit.index)
plt.show()

df['Order Date']= pd.to_datetime(df['Order Date'],format='%m/%d/%Y')
df['Year'] = df['Order Date'].dt.year
df['Month'] = df['Order Date'].dt.month

yearly_monthly_profit = df.groupby(['Year', 'Month'])['Profit'].sum().reset_index()

sns.lineplot(data=yearly_monthly_profit, x='Month', y='Profit', hue='Year', marker='o')
plt.xlabel('Month')
plt.ylabel('Profit')
plt.title('Monthly Profit Trend by Year')
plt.grid(True)
plt.xticks(range(1, 13)) # Ensure all month labels (1-12) are shown
plt.legend(title='Year')
plt.show()

"""-the store grows at steady pace, so the profit is increasing within the years


"""

df['Ship Date'] = pd.to_datetime(df['Ship Date'],format='%m/%d/%Y')
df['shiping time']= df['Ship Date']-df['Order Date']

# Calculate mode
groupship_mode = df.groupby('Category')['shiping time'].apply(lambda x: x.mode().iloc[0])
print("Mode of shipping time by Category:\n", groupship_mode)

# Calculate mean
groupship_mean = df.groupby('Category')['shiping time'].mean()
print("\nMean of shipping time by Category:\n", groupship_mean)

# Calculate max
groupship_max = df.groupby('Category')['shiping time'].max()
print("\nMax shipping time by Category:\n", groupship_max)

